class UpdateExpressionBuilder:
    def __init__(self, model_cls: Type[DatabaseModel]):
        self.SET = []
        self.ADD = []
        self.REMOVE = []
        self.DELETE = []
        self.expression_attribute_values = {}
        self.model_schema = model_cls.model_json_schema()
        self.expression_attribute_names = {}

    def _safe_name(self, key):
        """Replace reserved words with a safe placeholder."""
        if key.upper() in RESERVED_WORDS:
            safe_key = f"#n_{key}"
            self.expression_attribute_names[safe_key] = key
            return safe_key
        return key

    def _get_field_type(self, key):
        return self.model_schema["properties"].get(key, {}).get("type", None)

    def set(self, key, value, if_not_exists=False, list_append=None):
        safe_key = self._safe_name(key)
        placeholder = ":" + key
        if if_not_exists:
            self.SET.append(f"{safe_key} = if_not_exists({safe_key}, {placeholder})")
        elif list_append is not None:
            append_placeholder = ":append_" + key
            self.SET.append(f"{safe_key} = list_append({safe_key}, {append_placeholder})")
            self.expression_attribute_values[append_placeholder] = list_append
        else:
            self.SET.append(f"{safe_key} = {placeholder}")

        self.expression_attribute_values[placeholder] = value
        return self

    def add(self, key, value):
        safe_key = self._safe_name(key)
        data_type = self._get_field_type(key)
        if "." in key:
            raise ValueError("The ADD action cannot be used on nested attributes.")
        if data_type not in ["integer", "float", "number", "array"]:
            raise ValueError(f"The ADD action does not support '{data_type}' data type.")

        placeholder = ":" + key
        self.ADD.append(f"{safe_key} {placeholder}")
        self.expression_attribute_values[placeholder] = value
        return self

    def remove(self, key):
        safe_key = self._safe_name(key)
        self.REMOVE.append(safe_key)
        self.expression_attribute_names[safe_key] = key
        return self

    def delete(self, key, value):
        data_type = self._get_field_type(key)
        safe_key = self._safe_name(key)
        if data_type != "array":
            raise ValueError(f"The DELETE action only supports set data types. Got '{data_type}' instead.")

        placeholder = ":" + key
        self.DELETE.append(f"{safe_key} {placeholder}")
        self.expression_attribute_values[placeholder] = value
        self.expression_attribute_names[safe_key] = key
        return self

    def build(self) -> Tuple[str, dict[str, Any], dict[str, Any]]:
        expression_parts = []

        if self.SET:
            expression_parts.append("SET " + ", ".join(self.SET))
        if self.ADD:
            expression_parts.append("ADD " + ", ".join(self.ADD))
        if self.REMOVE:
            expression_parts.append("REMOVE " + ", ".join(self.REMOVE))
        if self.DELETE:
            expression_parts.append("DELETE " + ", ".join(self.DELETE))

        update_expression = " ".join(expression_parts)
        return (
            update_expression,
            self.expression_attribute_values,
            self.expression_attribute_names,
        )


class DatabaseModelUpdateExpressionBuilder(UpdateExpressionBuilder):
    def __init__(self, model_cls: Type[DatabaseModel], id: str, sort_key: Optional[str] = None):
        self.hash_key = id
        self.sort_key = sort_key
        self.model_cls = model_cls
        super().__init__(model_cls)

    def execute(self):
        return self.model_cls.update_item(self.hash_key, range_key=self.sort_key, update_builder=self)
